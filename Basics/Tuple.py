# Tuple declaration
fruitTuple = ("Apple", "Orange", "Banana", "Plum", "Banana")
print("Items in Tuple {} ".format(fruitTuple))

print("Type of object {}".format(type(fruitTuple)))

print("Banana repetition time : {}".format(fruitTuple.count("Banana")))

print("Index of Orange {}".format(fruitTuple.index("Orange")))
